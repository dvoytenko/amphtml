import '../../third_party/babel/custom-babel-helpers';

const React = require('../../node_modules/react/lib/ReactIsomorphic');
const CallbackQueue = require('../../node_modules/react/lib/CallbackQueue');
const PooledClass = require('../../node_modules/react/lib/PooledClass');
const Transaction = require('../../node_modules/react/lib/Transaction');
const assign = require('../../node_modules/react/lib/Object.assign');


/**
 */
var TRANSACTION_WRAPPERS = [];


/**
 * @class AmpReconcileTransaction
 */
export function AmpReconcileTransaction(forceHTML) {
  this.reinitializeTransaction();
  // Only server-side rendering really needs this option (see
  // `ReactServerRendering`), but server-side uses
  // `ReactServerRenderingTransaction` instead. This option is here so that it's
  // accessible and defaults to false when `AmpReactComponent` and
  // `ReactTextComponent` checks it in `mountComponent`.`
  this.renderToStaticMarkup = false;
  this.reactMountReady = CallbackQueue.getPooled(null);
  this.useCreateElement = false;
}

var Mixin = {
  /**
   * @see Transaction
   * @abstract
   * @final
   * @return {array<object>} List of operation wrap procedures.
   *   TODO: convert to array<TransactionWrapper>
   */
  getTransactionWrappers: function() {
    return TRANSACTION_WRAPPERS;
  },

  /**
   * @return {object} The queue to collect `onDOMReady` callbacks with.
   */
  getReactMountReady: function() {
    return this.reactMountReady;
  },

  /**
   * `PooledClass` looks for this, and will invoke this before allowing this
   * instance to be reused.
   */
  destructor: function() {
    CallbackQueue.release(this.reactMountReady);
    this.reactMountReady = null;
  },
};


assign(AmpReconcileTransaction.prototype, Transaction.Mixin, Mixin);
PooledClass.addPoolingTo(AmpReconcileTransaction);
